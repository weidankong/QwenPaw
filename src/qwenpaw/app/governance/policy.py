@dataclass
class GovernancePolicy:
    """Policy: builtin_rules + user_rules two layers, first-match-wins.

    Loaded from policy_dir/policy.yaml, held by ResourceGovernor.

    Evaluation flow (§5):
        ToolRegistry type check → builtin_rules → user_rules → global fallback

    Lifecycle:
        load → evaluate (hot path) → add_rule (user approve) → save
    """
    version: str = "1.0"
    builtin_rules: List[PolicyRule] = field(default_factory=list)
    user_rules: List[PolicyRule] = field(default_factory=list)
    audit_level: str = "all"       # "all" | "write_only" | "none"

    # Internal reference to registry (defaults to module-level DEFAULT_REGISTRY)
    _registry: ToolRegistry = field(
        default=None, repr=False, compare=False,
    )

    def __post_init__(self) -> None:
        if self._registry is None:
            self._registry = DEFAULT_REGISTRY

    # ------------------------------------------------------------------
    # Read-only view
    # ------------------------------------------------------------------

    @property
    def rules(self) -> List[PolicyRule]:
        """Merge builtin_rules + user_rules (read-only snapshot).

        Note: returns a snapshot; modifications will not affect the original list.
        Use add_rule() to add new rules.
        """
        return list(self.builtin_rules) + list(self.user_rules)

    def evaluate(
        self, tc_spec: ToolCallSpec,
    ) -> tuple[PolicyDecision, str]:
        """Evaluate policy decision for a tool call, returning the matched rule's reason.

        Evaluation flow (§5):
            0. ToolRegistry type check: unknown → DENY, internal → ALLOW
            1. builtin_rules first-match-wins
            2. user_rules first-match-wins
            3. Global fallback: shell → SANDBOX_FALLBACK, others → ASK

        Returns: (PolicyDecision, reason)
        """
        # ── Step 0: ToolRegistry type check ──
        tool_type = self._registry.get_type(tc_spec.tool_name)
        if tool_type == "unknown":
            return PolicyDecision.DENY, f"Unregistered tool: {tc_spec.tool_name}"
        if tool_type == "internal":
            return PolicyDecision.ALLOW, ""

        # ── Step 1: builtin_rules ──
        for rule in self.builtin_rules:
            if rule.matches_tool_call(
                tc_spec, tool_type=tool_type,
            ):
                return PolicyDecision(rule.action.value), rule.reason

        # ── Step 2: user_rules ──
        for rule in self.user_rules:
            if rule.matches_tool_call(
                tc_spec, tool_type=tool_type,
            ):
                return PolicyDecision(rule.action.value), rule.reason

        # ── Step 3: Global fallback ──
        if tool_type == "shell":
            return PolicyDecision.SANDBOX_FALLBACK, "sandbox fallback"
        return PolicyDecision.ASK, "No rule hit"

    def evaluate_source(self, tc_spec: ToolCallSpec) -> str:
        """Determine which rule source a tool call matches.

        Returns:
            "builtin"  — matched builtin_rules (no rule recorded on approve)
            "user"     — matched user_rules (rule can be recorded on approve)
            "fallback" — no match, global fallback
        """
        tool_type = self._registry.get_type(tc_spec.tool_name)
        for rule in self.builtin_rules:
            if rule.matches_tool_call(
                tc_spec, tool_type=tool_type,
            ):
                return "builtin"
        for rule in self.user_rules:
            if rule.matches_tool_call(
                tc_spec, tool_type=tool_type,
            ):
                return "user"
        return "fallback"

    # ------------------------------------------------------------------
    # Dynamic mutation (only operates on user_rules)
    # ------------------------------------------------------------------

    def add_rule(self, rule: PolicyRule) -> None:
        """Append a rule to user_rules (called after user approval).

        Note: builtin_rules are read-only and cannot be modified via this method.
        """
        self.user_rules.append(rule)

    def remove_rule(self, index: int) -> None:
        """Remove a rule from user_rules (Console UI / admin operation).

        Note: builtin_rules cannot be deleted via this method.
        """
        if 0 <= index < len(self.user_rules):
            self.user_rules.pop(index)


# ---------------------------------------------------------------------------
# Rule generalization
# ---------------------------------------------------------------------------

def generalize_rule_match(tool_name: str, target: str) -> str:
    """Construct a match string from an approved rule.

    Current strategy: no generalization, always record exact match
    to avoid security risks from wildcards.

    Args:
        tool_name: policy tool name, e.g. "Bash"
        target: tool's target argument value

    Returns:
        match string, e.g. "Bash(git status)"
    """
    return f"{tool_name}({target})"


# ---------------------------------------------------------------------------
# Helper functions
# ---------------------------------------------------------------------------

def _parse_match(match_str: str) -> tuple[str, str]:
    """Parse "ToolName(pattern)" → (tool_name, pattern).

    Uses index to find the first "(", since the tool name never contains "(".
    Uses rindex to find the last ")" to handle cases where pattern contains ")".
    Example: "Bash(git *)" → ("Bash", "git *")
             "*(.env*)"   → ("*", ".env*")      # wildcard for all tools
             "Read(src/**)" → ("Read", "src/**")
             "Bash(echo $(date))" → ("Bash", "echo $(date)")
    """
    paren = match_str.index("(")
    close = match_str.rindex(")")
    tool_name = match_str[:paren]
    pattern = match_str[paren + 1:close]
    return tool_name, pattern


# ---------------------------------------------------------------------------
# Load / persist
# ---------------------------------------------------------------------------

def load_governance_policy(policy_dir: str, workspace_dir: str) -> GovernancePolicy:
    """Load from policy_dir/policy.yaml; return default policy if missing.

    Args:
        policy_dir: directory containing policy.yaml
        workspace_dir: used to replace WORKSPACE_DIR placeholders in rules

    YAML format:
        version: "1.0"
        audit_level: "all"
        builtin_rules:
          - match: "*(.env*)"
            action: ask
            reason: "Env file may contain secrets/credentials"
        user_rules:
          - match: "Read(WORKSPACE_DIR/*)"
            action: allow
    """
    path = Path(policy_dir) / "policy.yaml"
    if not path.exists():
        return _create_default_policy(workspace_dir)

    try:
        with open(path, "r", encoding="utf-8") as f:
            data = yaml.safe_load(f)
    except Exception:
        return _create_default_policy(workspace_dir)

    if not isinstance(data, dict):
        return _create_default_policy(workspace_dir)

    version = data.get("version", "1.0")
    audit_level = data.get("audit_level", "all")

    # ── builtin_rules + user_rules ──
    builtin_rules = _parse_rules(data.get("builtin_rules", []))
    user_rules = _parse_rules(data.get("user_rules", []))

    # ── Cold start: fill in missing default rules ──
    if not builtin_rules:
        builtin_rules = copy.deepcopy(DEFAULT_BUILTIN_RULES)
    if not user_rules:
        user_rules = copy.deepcopy(DEFAULT_USER_RULES)

    # ── Replace WORKSPACE_DIR with actual path ──
    if workspace_dir:
        _resolve_workspace_dir(builtin_rules, workspace_dir)
        _resolve_workspace_dir(user_rules, workspace_dir)

    return GovernancePolicy(
        version=version,
        builtin_rules=builtin_rules,
        user_rules=user_rules,
        audit_level=audit_level,
    )


def save_governance_policy(policy: GovernancePolicy, policy_dir: str,
                           workspace_dir: str = "") -> None:
    """Write policy.yaml.

    Args:
        policy: policy to persist
        policy_dir: directory to write policy.yaml
        workspace_dir: workspace path, used to restore actual paths back to
                       WORKSPACE_DIR placeholders (keeps yaml portable)
    """
    builtin_rules = list(policy.builtin_rules)
    user_rules = list(policy.user_rules)

    # ── Restore actual paths to WORKSPACE_DIR placeholders ──
    if workspace_dir:
        _unresolve_workspace_dir(builtin_rules, workspace_dir)
        _unresolve_workspace_dir(user_rules, workspace_dir)

    path = Path(policy_dir) / "policy.yaml"
    data = {
        "version": policy.version,
        "audit_level": policy.audit_level,
        "builtin_rules": [
            _rule_to_dict(r) for r in builtin_rules
        ],
        "user_rules": [
            _rule_to_dict(r) for r in user_rules
        ],
    }

    with open(path, "w", encoding="utf-8") as f:
        yaml.dump(data, f, default_flow_style=False,
                  allow_unicode=True, sort_keys=False)


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _create_default_policy(workspace_dir: str = "") -> GovernancePolicy:
    """Create a policy with full default rules (cold start)."""
    import copy
    builtin_rules = copy.deepcopy(DEFAULT_BUILTIN_RULES)
    user_rules = copy.deepcopy(DEFAULT_USER_RULES)
    if workspace_dir:
        _resolve_workspace_dir(builtin_rules, workspace_dir)
        _resolve_workspace_dir(user_rules, workspace_dir)
    return GovernancePolicy(
        version="1.0",
        builtin_rules=builtin_rules,
        user_rules=user_rules,
    )


def _resolve_workspace_dir(rules: List[PolicyRule], workspace_dir: str):
    """Replace WORKSPACE_DIR in rules with actual paths (in-place)."""
    for rule in rules:
        if "WORKSPACE_DIR" in rule.match:
            rule.match = rule.match.replace("WORKSPACE_DIR", workspace_dir)


def _unresolve_workspace_dir(rules: List[PolicyRule], workspace_dir: str):
    """Restore actual paths in rules back to WORKSPACE_DIR placeholders."""
    for rule in rules:
        if workspace_dir in rule.match:
            rule.match = rule.match.replace(workspace_dir, "WORKSPACE_DIR")


def _parse_rules(items: Optional[list[dict[str, Any]]]) -> List[PolicyRule]:
    """Parse a list of PolicyRule from YAML list."""
    if not items:
        return []
    rules = []
    for item in items:
        if not isinstance(item, dict) or "match" not in item:
            continue
        action_str = item.get("action", "deny")
        try:
            action = PolicyAction(action_str)
        except ValueError:
            action = PolicyAction.DENY
        rules.append(PolicyRule(
            match=item["match"],
            action=action,
            reason=item.get("reason", ""),
            grantee=item.get("grantee", "*"),
            duration=item.get("duration", "permanent"),
            session_id=item.get("session_id"),
        ))
    return rules


def _rule_to_dict(rule: PolicyRule) -> dict[str, Any]:
    """Serialize a PolicyRule to dict (for YAML output)."""
    d = {
        "match": rule.match,
        "action": rule.action.value,
    }
    if rule.reason:
        d["reason"] = rule.reason
    if rule.grantee != "*":
        d["grantee"] = rule.grantee
    if rule.duration != "permanent":
        d["duration"] = rule.duration
    if rule.session_id:
        d["session_id"] = rule.session_id
    return d
